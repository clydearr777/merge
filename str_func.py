def title_all(stroka):
    """
    Функция, принимающая на вход строку и возвращающая её со всеми заглавными буквами
    """
    return stroka.upper()

def title(stroka):
    """
    Функция, принимающая на вход строку и возвращающая её с заглавно буквы каждое слово
    """
    return stroka.title()
